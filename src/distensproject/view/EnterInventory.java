    private void updateBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateBtnActionPerformed
        if(!addInventoryTxt.getText().equals("")){
            try{
                Inventory.currentInventory.setInventory(Inventory.currentInventory.getInventory()+
                    Integer.valueOf(addInventoryTxt.getText()));
                JOptionPane.showMessageDialog(this, "Inventario actualizado correctamente", "Agregado",
                            JOptionPane.INFORMATION_MESSAGE);
                this.dispose();
                Inventory inventory = new Inventory();
                inventory.setVisible(true);
            }catch(Exception e){
                JOptionPane.showMessageDialog(this, "Error al intentar actualizar el inventario", "Error",
                            JOptionPane.ERROR_MESSAGE);
            }
        }else{
            JOptionPane.showMessageDialog(this, "No se puede dejar este campo en blanco", "Error",
                            JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_updateBtnActionPerformed

