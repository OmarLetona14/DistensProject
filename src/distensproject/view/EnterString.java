    private void loadBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_loadBtnActionPerformed
        if(!cadenaTxtA.getText().equals("")){
            separator.readString(cadenaTxtA.getText(),this);
            JOptionPane.showMessageDialog(this, "Datos leidos correctamente", "Agregado",
                JOptionPane.INFORMATION_MESSAGE);
            this.dispose();
          
        }else{
            JOptionPane.showMessageDialog(this, "Por favor, introduzca una cadena", "Error",
                            JOptionPane.ERROR_MESSAGE);
        }
        
    }//GEN-LAST:event_loadBtnActionPerformed

